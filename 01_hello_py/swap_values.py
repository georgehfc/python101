# SWAP TWO VALUES GIVEN BY THE USER
print("### 1️⃣ 2️⃣ 3️⃣ SWAP 1️⃣ 2️⃣ 3️⃣ ###")
print("This script swaps two given values")
a = input("Type the first value: ")
b = input("Type the second value: ")
print("Calculating...")
swap = a
a = b
b = swap
print("Success! Values were swapped.")
print("The first value is {} and the second value is {}".format(a, b))
